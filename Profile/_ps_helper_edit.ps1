function file_replace ( $sf, $rg_find, $rg_repl = "" ){
    (gc $sf) -replace $rg_find, $rg_repl | sc $sf
}

function pipe_replace( $rg_find, $rg_repl = "" ){
    begin { ooiie 'pipe_replace ::: started !!!'} 
    process { file_replace  $_  $rg_find  $rg_repl }
    end { ooiie 'pipe_replace ::: completed !!!' } 
}




