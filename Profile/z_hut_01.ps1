function get_empty_vol {
    $vols = (Get-Volume).DriveLetter
    foreach ($vol in (crange D Z)) { if ($vols -notcontains $vol) { return $vol } }
}






# :::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::: 

$vol_empty = get_empty_vol 'O'
Set-Partition 1 2 -NewDriveLetter $vol_empty `
    | Format-Volume -FileSystem FAT32 -NewFileSystemLabel SYSTEM 
Remove-PartitionAccessPath  -DriveLetter $vol_empty  -AccessPath "${vol_empty}:\"





