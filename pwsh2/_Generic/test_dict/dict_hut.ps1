function dict_from_arr($arr_k, $arr_v) {
    $Type_key = $arr_k[0].GetType().name
    $Type_val = $arr_v[0].GetType().name
    $dict = New-Object ("Dictionary[$Type_key, $Type_val]")
    for ($i=0; $i -lt $arr_k.count; $i++) {
        $dict.Add($arr_k[$i], $arr_v[$i])
    }
    $dict
}

